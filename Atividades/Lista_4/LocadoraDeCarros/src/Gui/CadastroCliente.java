    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        if (!textCodigo.getText().equals("") && !textNome.getText().equals("") && !textRG.getText().equals("") && !textCPF.getText().equals("") && !textTelefone.getText().equals("")) {
            int id = Integer.parseInt(textCodigo.getText());
            String nome = textNome.getText();
            String rg = textRG.getText();
            String cpf = textCPF.getText();
            String telefone = textTelefone.getText();

            Cliente cliente = new Cliente(id, rg, cpf, nome, telefone);
            salvarClienteNoArquivo(cliente);
            this.setVisible(false);
            JOptionPane.showMessageDialog(null, "Cliente cadastrado com sucesso", "Aviso", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "Todos os campos devem ser inseridos!", "Aviso", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

