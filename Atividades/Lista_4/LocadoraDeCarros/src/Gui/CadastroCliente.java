    public void salvarClienteNoArquivo(Cliente cliente) {
        try (BufferedWriter writer = new BufferedWriter(new FileWriter("src/Resources/clientes.txt", true))) {
            writer.write(String.valueOf(cliente.getId()) + '.' + cliente.getRg() + '.' + cliente.getCpf() + '.' + cliente.getNome() + '.' + cliente.getTelefone() + ".\n");
        } catch (IOException e) {
            System.out.println("Erro ao salvar o arquivo: " + e.getMessage());
        }
    }

